// вҡ”пёҸ CГіdigo creado por рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё
// рҹӣЎпёҸ BALDWIND IV - DONAR AL CLAN

let handler = async (m, { conn, usedPrefix, text, command }) => {
  let user = global.db.data.users[m.sender]
  
  if (!user.clan) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO PERTENECES A NINGГҡN CLAN*\n> рҹ“Ң Usa *${usedPrefix}clanes* para ver los clanes disponibles\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  const amount = parseInt(text)
  if (isNaN(amount) || amount <= 0) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> рҹңё *DONAR AL CLAN*\n\nвңҰ рқ—Ёрқ—Ұрқ—ў вңҰ\n> рҹ“Ң *${usedPrefix + command} <cantidad>*\n\nрҹҺҜ *Ejemplo:*\n> ${usedPrefix + command} 1000\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  if ((user.coins || 0) < amount) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO TIENES SUFICIENTES MONEDAS*\n> рҹ’° Necesitas: ${amount} monedas\n> рҹӘҷ Tienes: ${user.coins || 0}\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  const clan = global.db.data.clans[user.clan]
  if (!clan) {
    user.clan = null
    await global.db.write()
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *EL CLAN YA NO EXISTE*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  // Donar
  user.coins -= amount
  clan.coins = (clan.coins || 0) + amount
  
  // Ganar EXP de clan (1 EXP por cada 10 monedas donadas)
  const expGanada = Math.floor(amount / 10)
  clan.exp = (clan.exp || 0) + expGanada
  
  // Verificar subida de nivel del clan
  let nivelUp = false
  let nivel = clan.level || 1
  let expNecesaria = nivel * 1000
  
  if (clan.exp >= expNecesaria) {
    clan.level = nivel + 1
    clan.exp = clan.exp - expNecesaria
    nivelUp = true
  }
  
  await global.db.write()
  
  let textMsg = `вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n`
  textMsg += `> рҹ’° *DONACIГ“N REALIZADA* рҹ’°\n\n`
  textMsg += `вңҰ рқ——рқ—ҳрқ—§рқ—”рқ—ҹрқ—ҹрқ—ҳрқ—Ұ вңҰ\n`
  textMsg += `> рҹҸ° *Clan:* ${clan.name}\n`
  textMsg += `> рҹ’ё *Donaste:* ${amount} monedas\n`
  textMsg += `> рҹ“Ҳ *EXP de clan:* +${expGanada}\n`
  textMsg += `> рҹ’° *Cofre del clan:* ${clan.coins} monedas\n`
  
  if (nivelUp) {
    textMsg += `\nвңЁ *ВЎEL CLAN HA SUBIDO DE NIVEL!* вңЁ\n`
    textMsg += `> рҹ“Ҡ *Nuevo nivel:* ${clan.level}\n`
    textMsg += `> рҹ’° *Bono monedas:* +${5 + (clan.level - 1) * 2}%\n`
    textMsg += `> рҹ“ҡ *Bono EXP:* +${(clan.level - 1) * 1.5}%\n`
  }
  
  textMsg += `\nв§јвӢҶкҷівҖўгҖ” рҹӣё рқ—•рқ—”рқ—ҹрқ——рқ—Әрқ—ңрқ—Ўрқ—— рқ—ңрқ—© гҖ•вӢҶкҷівҖўв§Ҫ\n`
  textMsg += `> рҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*\n`
  textMsg += `в•°вӢҶкҷівҖўвқ…вҖ§*вӮҠвӢҶкҷіпёҺвҖ§*вқҶвӮҠвӢҶв•Ҝ\n`
  textMsg += `вҢ¬ КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ  бҙ„КҸКҷбҙҮКҖ бҙҚбҙҮЙҙбҙң рҹ§¬`
  
  await conn.sendMessage(m.chat, { text: textMsg }, { quoted: m })
}

handler.help = ['donar <cantidad>']
handler.tags = ['clan']
handler.command = ['donar', 'donateclan']
handler.register = false

export default handler