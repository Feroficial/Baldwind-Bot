// вҡ”пёҸ CГіdigo creado por рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё
// рҹӣЎпёҸ BALDWIND IV - EXPULSAR DEL CLAN

let handler = async (m, { conn, usedPrefix, text, command }) => {
  let user = global.db.data.users[m.sender]
  
  if (!user.clan) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO PERTENECES A NINGГҡN CLAN*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  let mentioned = m.mentionedJid && m.mentionedJid[0]
  if (!mentioned && text) {
    mentioned = text.replace(/[^0-9]/g, '') + '@s.whatsapp.net'
  }
  
  if (!mentioned) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> рҹңё *EXPULSAR DEL CLAN*\n\nвңҰ рқ—Ёрқ—Ұрқ—ў вңҰ\n> рҹ“Ң *${usedPrefix + command} @tag*\n\nрҹҺҜ *Ejemplo:*\n> ${usedPrefix + command} @usuario\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  if (mentioned === m.sender) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO PUEDES EXPULSARTE A TI MISMO*\n> рҹ“Ң Usa *${usedPrefix}salirclan*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  const clan = global.db.data.clans[user.clan]
  if (!clan) {
    user.clan = null
    await global.db.write()
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *EL CLAN YA NO EXISTE*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  // Verificar rango (solo lГӯder y co-lГӯder pueden expulsar)
  const rango = user.clanRank || 'miembro'
  const rangosPermitidos = ['lГӯder', 'co-lГӯder']
  if (!rangosPermitidos.includes(rango)) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO TIENES PERMISO PARA EXPULSAR*\n> рҹ“Ң Solo lГӯder y co-lГӯder pueden expulsar\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  // Verificar que el usuario estГ© en el clan
  if (!clan.members.includes(mentioned)) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *EL USUARIO NO PERTENECE A TU CLAN*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  // No se puede expulsar al lГӯder
  if (mentioned === clan.leader) {
    return m.reply(`вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n> вқҢ *NO PUEDES EXPULSAR AL LГҚDER DEL CLAN*\n\nрҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*`)
  }
  
  // Expulsar
  clan.members = clan.members.filter(m => m !== mentioned)
  const expelledUser = global.db.data.users[mentioned]
  if (expelledUser) {
    expelledUser.clan = null
    expelledUser.clanRank = null
  }
  
  await global.db.write()
  
  let textMsg = `вҖ”НһНҹНһНҹ   *рҹңё КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ   рҹӣё  бҙ„КҸКҷбҙҮКҖ бҙ„бҙҸКҖбҙҮ  рҹңё* В»\n`
  textMsg += `> рҹҡ« *USUARIO EXPULSADO* рҹҡ«\n\n`
  textMsg += `вңҰ рқ——рқ—ҳрқ—§рқ—”рқ—ҹрқ—ҹрқ—ҳрқ—Ұ вңҰ\n`
  textMsg += `> рҹҸ° *Clan:* ${clan.name}\n`
  textMsg += `> рҹ‘Ө *Expulsado:* @${mentioned.split('@')[0]}\n`
  textMsg += `> рҹ‘‘ *Expulsado por:* @${m.sender.split('@')[0]}\n\n`
  textMsg += `в§јвӢҶкҷівҖўгҖ” рҹӣё рқ—•рқ—”рқ—ҹрқ——рқ—Әрқ—ңрқ—Ўрқ—— рқ—ңрқ—© гҖ•вӢҶкҷівҖўв§Ҫ\n`
  textMsg += `> рҹ‘‘ *рҹңё рқҳҝрқҷҖрқҷ‘рқҷҮрқҷ”рқҷҠрқҷүрқҷү рҹңё*\n`
  textMsg += `в•°вӢҶкҷівҖўвқ…вҖ§*вӮҠвӢҶкҷіпёҺвҖ§*вқҶвӮҠвӢҶв•Ҝ\n`
  textMsg += `вҢ¬ КҷбҙҖКҹбҙ…бҙЎЙӘЙҙбҙ… ЙӘбҙ  бҙ„КҸКҷбҙҮКҖ бҙҚбҙҮЙҙбҙң рҹ§¬`
  
  await conn.sendMessage(m.chat, { text: textMsg, mentions: [m.sender, mentioned] }, { quoted: m })
}

handler.help = ['expulsar @tag']
handler.tags = ['clan']
handler.command = ['expulsar', 'kickclan']
handler.register = false

export default handler